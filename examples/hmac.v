const (
	ipad      = []byte{len: 256, init: 0x36} // TODO is 256 enough??
	opad      = []byte{len: 256, init: 0x5C}
	npad      = []byte{len: 256, init: 0}
)

// returns a HMAC based hash, byte array, depending on the used hash algo
fn encode(hash_func fn(bytes []byte) []byte, blocksize int, key []byte, text []byte) []byte {
	mut b_key := if key.len <= blocksize { key } else { hash_func(key) }
	if b_key.len < blocksize {
		b_key << npad[..blocksize - b_key.len]
	}

	mut inner := []byte{}
	for i, b in ipad[..blocksize] {
		inner << b_key[i] ^ b
	}

	inner << text
	inner_hash := hash_func(inner)

	mut outer := []byte{cap: b_key.len}
	for i, b in opad[..blocksize] {
		outer << b_key[i] ^ b
	}

	outer << inner_hash
	digest := hash_func(outer)
	return digest
}

