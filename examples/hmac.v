import crypto.md5
import crypto.sha1

const (
	ipad = []byte{len:256, init:0x36}  // TODO is 256 enough??
	opad = []byte{len:256, init:0x5C}
	npad = []byte{len:256, init:0}
	blocksize = 64  // varys depening on the used hashing algo
)

fn encode_md5(key []byte, text []byte) string {
	mut b_key := if key.len <= blocksize {key} else {md5.sum(key)}
    // (1) append zeros to the end of K to create a B byte string
        // (e.g., if K is of length 20 bytes and B=64, then K will be
         // appended with 44 zero bytes 0x00)
	if b_key.len < blocksize {b_key << npad[..blocksize-b_key.len]}
    // (2) XOR (bitwise exclusive-OR) the B byte string computed in step
        // (1) with ipad
	mut inner := []byte{}
	for i, b in ipad[..blocksize] { inner << b_key[i]^b }
    // (3) append the stream of data 'text' to the B byte string resulting
        // from step (2)
	inner << text
	// (4) apply H to the stream generated in step (3)
	inner_hash := md5.sum(inner)
    // (5) XOR (bitwise exclusive-OR) the B byte string computed in
        // step (1) with opad
	mut outer := []byte{cap:b_key.len}
	for i, b in opad[..blocksize] { outer << b_key[i]^b }
    // (6) append the H result from step (4) to the B byte string
        // resulting from step (5)
	outer << inner_hash
    // (7) apply H to the stream generated in step (6) and output
        // the result
	digest := md5.sum(outer)

	return digest.hex()
}

fn encode_sha1(key []byte, text []byte) string {
	mut b_key := if key.len <= blocksize {key} else {sha1.sum(key)}
	if b_key.len < blocksize {b_key << npad[..blocksize-b_key.len]}

	mut inner := []byte{}
	for i, b in ipad[..blocksize] { inner << b_key[i]^b }
	inner << text
	inner_hash := sha1.sum(inner)

	mut outer := []byte{cap:b_key.len}
	for i, b in opad[..blocksize] { outer << b_key[i]^b }
	outer << inner_hash

	digest := sha1.sum(outer)
	return digest.hex()
}

